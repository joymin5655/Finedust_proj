export function getPolicyEffect(policyId: string): PolicyEffect | undefined {
  return policyEffects.find((effect) => effect.policyId === policyId);
}

export function getPoliciesByCountry(countryCode: string): Policy[] {
  return policies.filter((policy) => policy.countryCode === countryCode);
}

export function getPoliciesByCategory(category: PolicyCategory): Policy[] {
  return policies.filter((policy) => policy.category === category);
}
